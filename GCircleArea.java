public class GCircleArea
{
  public static void main(String [] args)
   {
     int r = 14;
     float pi = 3.14f;
     float area = (pi * r * r);
     System.out.println("Area of circle");
     System.out.println(area);

     System.out.println("circumference of  circle");
     float cir = 2 * pi * r;
     System.out.println(cir); 
   }

}