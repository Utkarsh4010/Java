public class SwitchP1
{
   public static void main(String [] args)


   {
     int a = 20;
    switch(a)
     {
     case 1:   case 2: case 3:
     
      System.out.println("This is a number");
      break;

     
     case 4:   case 5:  case 6:
     
       System.out.println("This is a ");
       break;
    
    
     default:
     

     System.out.println("invalid");
     }
     
   } 

}